package ep3_col_cristaisigue_erickvasquez;

//La clase cabeza de res
public class CabezaRes {

	//Atributos
	private String ganaderiaPerteneciente;
	private double peso;
	private String sello;
	
	//Constructor
	public CabezaRes() {
		
	}

	
	//Set & Get
	
	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public String getSello() {
		return sello;
	}

	public void setSello(String sello) {
		this.sello = sello;
	}

	public String getGanaderiaPerteneciente() {
		return ganaderiaPerteneciente;
	}

	public void setGanaderiaPerteneciente(String ganaderiaPerteneciente) {
		this.ganaderiaPerteneciente = ganaderiaPerteneciente;
	}
	
	
}
